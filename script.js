window.addEventListener('scroll', function() {
  var scrollPosition = window.scrollY;

  // Homepage transition
  var maxScroll = 160; // adjust this value as needed

  var homepageOpacity = 1 - scrollPosition / maxScroll;
  var nextSectionOpacity = 0;

  if (homepageOpacity < 0) {
    homepageOpacity = 0;
    nextSectionOpacity = (scrollPosition - maxScroll) / maxScroll;
  }

  if (nextSectionOpacity > 1) nextSectionOpacity = 1;

  document.getElementById("homepage").style.opacity = homepageOpacity;
  document.getElementById("nextSection").style.opacity = nextSectionOpacity;

  // Sticky navbar effect
  var header = document.querySelector("header");
  header.classList.toggle("sticky", window.scrollY > 0);
});

